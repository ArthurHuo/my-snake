package com.ml.man;

public class SnakePoint {

	private int x;
	private int y;

	// private static final int edge = 1;
	public SnakePoint() {
	}

	public SnakePoint(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
